/*:=MPB=:=======================================================*
 *
 * Global Function: deriveVisu
 * Description:
 *	Fws dependant visu creation.
 * Interface:							*/
void deriveVisu(void)
/*:=MPE=:=======================================================*/
{
  DB_MSG (("Entered deriveVisu()"));

  /* Standard VISU Derivation */
  ParxRelsParRelations("VisuDerivePars", Yes);

  if (0 == VisuFileVersion)
  {
    if (PVM_FwsLoop==2)
    {
      char * comments = (char *)calloc(2, 40 * sizeof(char));
      if (PVM_FwsRecoMode==FWS_DEFAULT)
      {
        strcpy(&comments[0 * 40],"In Phase");
        strcpy(&comments[1 * 40],"Opposed Phase");

      }
      if (PVM_FwsRecoMode==FWS_SEPARATION)
      {
        strcpy(&comments[0 * 40],"Fat");
        strcpy(&comments[1 * 40],"Water");
      }
      PTB_VisuSetFGElementComment(
        PV_IDL_CONSTANT(visuid_framegroup_CYCLE),
        comments, 2, 40);

      free(comments);
    }
  }

  DB_MSG (("Leave deriveVisu()"));
}


